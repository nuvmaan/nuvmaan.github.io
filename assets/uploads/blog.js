document.addEventListener("DOMContentLoaded", async () => {
    const blogGrid = document.getElementById("blog-grid");

    try {
        const response = await fetch("/blogs/index.json"); // Fetch the JSON file generated by Netlify CMS
        const blogs = await response.json();

        blogs.forEach((blog) => {
            const blogCard = `
                <div class="col-md-4 mb-4">
                    <div class="card h-100">
                        <img src="${blog.image}" class="card-img-top" alt="${blog.title}">
                        <div class="card-body">
                            <h5 class="card-title">${blog.title}</h5>
                            <p class="card-text">${blog.excerpt}</p>
                            <a href="/blogs/${blog.slug}" class="btn btn-primary">Read More</a>
                        </div>
                    </div>
                </div>
            `;
            blogGrid.innerHTML += blogCard;
        });
    } catch (error) {
        console.error("Error fetching blogs:", error);
        blogGrid.innerHTML = `<p class="text-center text-danger">Failed to load blogs. Please try again later.</p>`;
    }
});

